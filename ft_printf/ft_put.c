#include "ft_printf.h"

void	ft_putchar_c(char c)
{
	write(1, &c, 1);
}

void	ft_putstr_s(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		ft_putchar_c(str[c]);
		c++;
	}
}
